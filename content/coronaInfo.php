    <h1>Es geht weiter</h1>
    <p>Liebe Fahrschüler,
    endlich soll es soweit sein, dass wir ab Montag vollumfänglich den Fahrschulbetrieb wieder aufnehmen dürfen. Leider haben wir immer noch keine Vorgaben von der 
    Landesregierung bekommen, welche Schutz- und Hygienemaßnahmen umgesetzt werden müssen. Was als sicher anzunehmen ist, dass unsere Räumlichkeiten nur mit Schutzmaske 
    betreten werden dürfen. Des Weiteren möchten wir euch bitten, bei Anmeldungen auf die Begleitung eurer Eltern zu verzichten, um das Infektionsrisiko so gering wie 
    möglich zu halten. Im Eingangsbereich werden wir Listen auslegen, in denen ihr euren Namen und eine Telefonnummer hinterlassen müsst, um mögliche Infektionsketten 
    zurück verfolgen zu können. Ebenso möchten wir euch um Verständnis bitten, dass wir das Aufkommen während der ersten Öffnungszeiten unserer Büros, unter Umständen 
    nicht komplett abarbeiten können. Wenn es Zeitlich möglich ist, wäre eine Anmeldung in der zweiten Öffnungswoche zu empfehlen. Auf Grund der entstandenen wirtschaftlich 
    angespannten Situationen, können alle Leistungen nur gegen Vorkasse abgewickelt werden. Grundsätzlich werden dieselben Bedingungen für theoretischen und praktischen 
    Unterricht gelten wie überall: Bei Anzeichen von Fieber, Unwohlsein oder Grippe, ist von der Teilnahme abzusehen.</p>

    <h2>Öffnungszeiten und Theorieunterrichte:</h2>
    <p>Wir planen in Schafflund am Montag den 18.05.2020 um 17 Uhr unser Büro wieder zu öffnen. Der theoretische Unterricht beginnt am selben Tag um 19 Uhr.</p>
    
    <p>In Großenwiehe öffnen wir am Dienstag den 19.05.2020 um 17 Uhr unser Büro und der Theoretische Unterricht beginnt um 19 Uhr.</p>
    
    <p>In Tarp öffnen wir am Donnerstag den 18.05.2020 um 17 Uhr unser Büro und beginnen den Unterricht um 19 Uhr.</p>

    <p>Wenn alles Problemlos umgesetzt werden kann, werden wir im altbekannten Turnus weitermachen. Der neue Lektionen Plan wird bis zum Wochenende von uns überarbeitet und 
    Online gestellt. Sollten zu viele Schüler kommen und die Abstandsregeln nicht eingehalten werden können, wiederholen wir um 21 Uhr die gleiche Lektion, um allen Schülern 
    die Möglichkeit zu eröffnen den Unterricht besuchen zu können.</p>

    <h2>Praktische Fahrausbildung:</h2>
    <p>auch hier ist noch nicht geklärt, wie die Fahrstunden ablaufen sollen. Wir hoffen, die offenen Fragen bis Montag klären zu können. Alle Fahrschüler, die fahren möchten, 
    können sich ab sofort bei ihrem Fahrlehrer/in melden und Termine vereinbaren. Um für euch eine höchstmögliche Effizienz der Fahrstunde zu gewährleisten, werden wir in den 
    nächsten Monaten alle Fahrstunden der Klasse B, beim Parkplatz des Deutschen Hauses starten und enden lassen. Dadurch möchten wir zeitraubende Leerfahrten vermeiden.</p>

    <p>Wir danken schon im Voraus für euer Verständnis und freuen uns, euch endlich wieder ausbilden zu dürfen!</p>
