<h3>Weiterbildung für Führerscheininhaber</h3>
<br>
<p>
Haben Sie sich schon einmal gefragt, ob ihr Fahrstil noch zum 
heutigen Stand der Technik passt?
<p>
Interessieren Sie sich für die neueste Technik in Bezug auf 
kraftstoffsparendes Fahren und Nutzung der Fahrerassistenzsysteme?
Oder möchten Sie einfach nur wieder sicherer im Straßenverkehr 
mitfahren können?
</p>
<p>
Wir bieten Ihnen ein Tagesseminar an, in dem Sie energiesparendes 
Fahren kennen lernen können und machen Sie mit den neuesten 
Fahrerassistenzsystemen vertraut.
<p>
Zum Beispiel: Einparkassistent, Geschwindigkeitsregelanlage, 
Tempomat, Spurhalteassistent, Aktiv-Citystopp, Lichtassistent,
Müdigkeitswarner, Start-Stopp-Technik.
Erkundigen Sie sich gleich bei uns und melden Sie sich an.</p>
<br>

<h3>Elterninfoabend-Was ist eigentlich BF-17?</h3>
<br>
<p>Haben Sie Fragen zum Thema begleitetes Fahren mit 17 (BF17)?
Wollen Sie, dass Ihre Tochter/Ihr Sohn den Führerschein mit 17
macht, wissen aber nicht, was das für Sie bedeutet?
Oder würden Sie gerne wissen, was der Führerschein mit 17 für 
Vor- bzw. Nachteile mit sich bringt?</p>

<p>Dann melden Sie sich doch einfach unverbindlich und kostenlos zu 
unserem Informationsabend zum Thema begleitetes Fahren mit 17 
(BF17) bei uns an. Erfahren Sie alles was wichtig ist und erkundigen
Sie sich nach allem, was Sie wissen wollen.</p>

<br>
<h3>BFK-Weiterbildung</h3>
<br>
<p>Müssen Sie in den nächsten zwei Jahren Ihre C/CE oder D/DE
Fahrerlaubnis verlängern lassen? Dann denken Sie daran,
dass Sie an den fünf Modulen der Berufskraftfahrerweiterbildung
teilgenommen haben müssen. Dieses betrifft alle, die 2012 oder 2013
ihren Führerschein verlängern müssen, weil sie entweder 50 Jahre
alt werden oder 2007 oder 2008 ihren C/CE Führerschein
nach neuem Recht gemacht haben und nun durch den Ablauf der
Fünfjahresfrist ihre Fahrerlaubnis verlängern müssen.</p>

