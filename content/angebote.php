<h3>BFK-Weiterbildung</h3>
<br>
<p>Müssen Sie in den nächsten zwei Jahren Ihre C/CE oder D/DE
Fahrerlaubnis verlängern lassen? Dann denken Sie daran,
dass Sie an den fünf Modulen der Berufskraftfahrerweiterbildung
teilgenommen haben müssen. Dieses betrifft alle, die 2012 oder 2013
ihren Führerschein verlängern müssen, weil sie entweder 50 Jahre
alt werden oder 2007 oder 2008 ihren C/CE Führerschein
nach neuem Recht gemacht haben und nun durch den Ablauf der
Fünfjahresfrist ihre Fahrerlaubnis verlängern müssen.</p>

