<h3>Weiterbildung für Führerscheininhaber</h3>
<br>
<p>
Haben Sie sich schon einmal gefragt, ob ihr Fahrstil noch zum 
heutigen Stand der Technik passt?
<p>
Interessieren Sie sich für die neueste Technik in Bezug auf 
kraftstoffsparendes Fahren und Nutzung der Fahrerassistenzsysteme?
Oder möchten Sie einfach nur wieder sicherer im Straßenverkehr 
mitfahren können?
</p>
<p>
Wir bieten Ihnen ein Tagesseminar an, in dem Sie energiesparendes 
Fahren kennen lernen können und machen Sie mit den neuesten 
Fahrerassistenzsystemen vertraut.
<p>
Zum Beispiel: Einparkassistent, Geschwindigkeitsregelanlage, 
Tempomat, Spurhalteassistent, Aktiv-Citystopp, Lichtassistent,
Müdigkeitswarner, Start-Stopp-Technik.
Erkundigen Sie sich gleich bei uns und melden Sie sich an.</p>
